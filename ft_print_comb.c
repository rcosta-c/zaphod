#include <unistd.h>

void	printer(char x[])
{
	if (x[0] == '7' && x[1] == '8' && x[2] == '9')
	{
		write(1, x, 3);
	}
	else
	{
		write(1, x, 3);
		write(1, ", ", 2);
	}
}

void	ft_print_comb(void)
{
	char	n[3];

	n[0] = '0';
	while (n[0] <= '7')
	{
		n[1] = n[0] + 1;
		while (n[1] <= '8')
		{
			n[2] = n[1] + 1;
			while (n[2] <= '9')
			{
				printer(n);
				n[2]++;
			}
			n[1]++;
		}
		n[0]++;
	}
}
